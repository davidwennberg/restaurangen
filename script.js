/*Öppnar sidonav*/
function openNav() {
    document.getElementById("mySidenav").style.width = "250px";
    document.body.style.backgroundColor = "rgb(0, 31, 21)";
  }
  /*Stänger sidonav*/
  function closeNav() {
    document.getElementById("mySidenav").style.width ="0";
    document.body.style.backgroundColor = "rgb(0, 61, 41)";
  }